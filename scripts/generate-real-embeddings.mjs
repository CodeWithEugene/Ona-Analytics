#!/usr/bin/env node
/**
 * Generate real 1536-dim embeddings for camp SOPs using NVIDIA's embedding API.
 *
 * Usage:
 *   1. Ensure NVIDIA_API_KEY is set in your environment
 *   2. node scripts/generate-real-embeddings.mjs
 *   3. This regenerates lib/db/seed-embeddings.sql with real vectors
 *
 * The embedded NVIDIA API key from .env.local is used if available.
 */

import fs from "fs"
import path from "path"
import { fileURLToPath } from "url"

const __dirname = path.dirname(fileURLToPath(import.meta.url))

// Load .env.local manually if present
let NVIDIA_API_KEY = process.env.NVIDIA_API_KEY || ""
const envLocalPath = path.resolve(__dirname, "..", ".env.local")
if (!NVIDIA_API_KEY && fs.existsSync(envLocalPath)) {
  const envContent = fs.readFileSync(envLocalPath, "utf-8")
  const match = envContent.match(/NVIDIA_API_KEY=(.+)/)
  if (match) NVIDIA_API_KEY = match[1].trim()
}

if (!NVIDIA_API_KEY) {
  console.error("ERROR: NVIDIA_API_KEY not found. Set it in your environment or .env.local")
  process.exit(1)
}

const CONTENTS = [
  "Weather SOP: If heavy rain is forecasted, supply trucks must depart 12 hours earlier due to severe mud on the Sekenani gate road.",
  "Fresh Supply SOP: Each occupied tent requires 4kg of fresh produce daily. For 25 tents at full occupancy, procure 100kg of mixed vegetables and fruits every 3 days.",
  "Gas and Fuel SOP: Each gas cylinder serves 5 tented suites for 7 days. During high occupancy (>80%), increase propane delivery by 50% and schedule bi-weekly refills.",
  "Linens SOP: Laundry turnaround requires 24 hours. At >90% occupancy, maintain 3 full sets of linens per tent (one on bed, one in laundry, one in storage).",
]

async function generateEmbedding(text, retries = 3) {
  for (let attempt = 1; attempt <= retries; attempt++) {
    try {
      console.log(`  Generating embedding for: "${text.substring(0, 60)}..."`)
      const response = await fetch(
        "https://integrate.api.nvidia.com/v1/embeddings",
        {
          method: "POST",
          headers: {
            Authorization: `Bearer ${NVIDIA_API_KEY}`,
            "Content-Type": "application/json",
          },
          body: JSON.stringify({
            model: "nvidia/nv-embedqa-e5-v5",
            input: [text],
            input_type: "query",
          }),
        }
      )

      if (!response.ok) {
        const errBody = await response.text()
        throw new Error(`API ${response.status}: ${errBody}`)
      }

      const data = await response.json()
      const embedding = data.data?.[0]?.embedding

      if (!embedding || !Array.isArray(embedding)) {
        throw new Error("Unexpected API response format")
      }

      if (embedding.length !== 1536) {
        console.warn(`  Warning: got ${embedding.length}-dim vector, padding/truncating to 1536`)
        if (embedding.length < 1536) {
          return [...embedding, ...new Array(1536 - embedding.length).fill(0)]
        }
        return embedding.slice(0, 1536)
      }

      return embedding
    } catch (err) {
      console.error(`  Attempt ${attempt}/${retries} failed: ${err.message}`)
      if (attempt === retries) throw err
      await new Promise((r) => setTimeout(r, 2000 * attempt))
    }
  }
}

function formatVectorSQL(vector) {
  const lines = []
  for (let i = 0; i < vector.length; i += 12) {
    const chunk = vector.slice(i, i + 12)
    const formatted = chunk.map((v) => {
      // Format to 4 decimal places for readability, preserving precision
      if (v === 0) return "0"
      // Use toFixed for small numbers but avoid scientific notation
      const s = v.toFixed(6)
      // Remove trailing zeros
      return parseFloat(s).toString()
    })
    lines.push("    " + formatted.join(","))
  }
  return "  ARRAY[\n" + lines.join(",\n") + "\n  ]::vector"
}

async function main() {
  console.log("")
  console.log("=== Ona Analytics: Real Embedding Generator ===")
  console.log(`Generating ${CONTENTS.length} embeddings via NVIDIA nv-embedqa-e5-v5\n`)

  const embeddings = []

  for (let i = 0; i < CONTENTS.length; i++) {
    console.log(`[${i + 1}/${CONTENTS.length}] Processing SOP...`)
    const embedding = await generateEmbedding(CONTENTS[i])
    embeddings.push(embedding)
    console.log(`  ✓ ${embedding.length}-dim vector generated\n`)
  }

  // Build SQL
  let sql = ""
  for (let i = 0; i < embeddings.length; i++) {
    const vec = formatVectorSQL(embeddings[i])
    sql +=
      "  (gen_random_uuid(), '11111111-1111-1111-1111-111111111111'::uuid,\n" +
      "   '" +
      CONTENTS[i].replace(/'/g, "''") +
      "',\n" +
      vec +
      ")"
    if (i < embeddings.length - 1) sql += ",\n\n"
    else sql += ";\n"
  }

  const fullSql = `-- Ona Analytics: Context Knowledge Embeddings
-- AUTO-GENERATED by scripts/generate-real-embeddings.mjs
-- Real 1536-dim vectors via NVIDIA nv-embedqa-e5-v5
-- Generated: ${new Date().toISOString()}

INSERT INTO context_knowledge (id, org_id, content, embedding)
VALUES

${sql}
`

  const outputPath = path.resolve(__dirname, "..", "lib/db/seed-embeddings.sql")
  fs.writeFileSync(outputPath, fullSql)
  console.log(`✓ Written ${outputPath}`)
  console.log(`  ${fullSql.split("\n").length} lines`)
  console.log(`  ${embeddings.length} embeddings, each ${embeddings[0].length} dimensions`)
  console.log("")
  console.log("=== Done ===")
  console.log("Run 'psql yourdb < lib/db/seed-embeddings.sql' to apply, or POST /api/migrate")
  console.log("")
}

main().catch((err) => {
  console.error("FATAL:", err)
  process.exit(1)
})
